package swingtree;

import java.awt.event.MouseEvent;
import java.awt.event.MouseListener;

class AdvancedSurfaceListener implements MouseListener {

    @Override
    final public void mouseClicked(MouseEvent e) {}

    @Override
    final public void mousePressed(MouseEvent e) {}

    @Override
    final public void mouseReleased(MouseEvent e) {}

    @Override
    public void mouseEntered(MouseEvent e) {}

    @Override
    public void mouseExited(MouseEvent e) {}

    public void mouseEntered(Object surface, MouseEvent e) {}

    public void mouseExited(Object surface, MouseEvent e) {}

}
